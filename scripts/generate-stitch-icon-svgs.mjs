/**
 * One-off generator: node scripts/generate-stitch-icon-svgs.mjs
 * Writes lib/stitch-templates/material-icon-svgs.ts
 */
import { writeFileSync } from "fs";
import { createElement } from "react";
import { renderToStaticMarkup } from "react-dom/server";
import {
  Activity,
  ArrowRight,
  BarChart3,
  Calendar,
  ChevronDown,
  ChevronLeft,
  ChevronRight,
  CircleCheck,
  Cloud,
  Database,
  Factory,
  Globe,
  HeartHandshake,
  Languages,
  Lightbulb,
  Mail,
  Plug,
  Quote,
  RefreshCw,
  Search,
  Share2,
  Shield,
  Star,
  Table2,
  Terminal,
  X,
} from "lucide-react";

const ICONS = {
  analytics: BarChart3,
  api: Plug,
  arrow_forward: ArrowRight,
  arrow_forward_ios: ChevronRight,
  calendar_today: Calendar,
  check_circle: CircleCheck,
  chevron_left: ChevronLeft,
  chevron_right: ChevronRight,
  close: X,
  cloud_sync: Cloud,
  database: Database,
  dataset: Table2,
  expand_more: ChevronDown,
  format_quote: Quote,
  insights: Lightbulb,
  language: Languages,
  mail: Mail,
  monitoring: Activity,
  precision_manufacturing: Factory,
  public: Globe,
  refresh: RefreshCw,
  search: Search,
  security: Shield,
  share: Share2,
  shield_with_heart: HeartHandshake,
  star: Star,
  terminal: Terminal,
};

const lines = [
  "// Generated by scripts/generate-stitch-icon-svgs.mjs — do not edit by hand.",
  "export const STITCH_MATERIAL_ICON_SVGS: Record<string, string> = {",
];

for (const [name, Icon] of Object.entries(ICONS)) {
  const svg = renderToStaticMarkup(
    createElement(Icon, { className: "__CLASS__", "aria-hidden": true }),
  );
  lines.push(`  ${JSON.stringify(name)}: ${JSON.stringify(svg)},`);
}

lines.push("};");
lines.push("");

writeFileSync(
  new URL("../lib/stitch-templates/material-icon-svgs.ts", import.meta.url),
  lines.join("\n"),
);
console.log("Wrote lib/stitch-templates/material-icon-svgs.ts");
